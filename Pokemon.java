    // Needs to be finished! Needs the class for the moves array, this only uses
    // "tackle" with 20 damage.
    public boolean takeDamage(Moves move) {
        double chance = Math.random() * (10 - 1 - 1);
        if (chance <= 6) {
            HP = HP - move.getMoveAttack();
            if (HP <= 0) {
                isFainted = true;
                return true;
            }
        }
        return false;
    }

    

